# Czym się różni argument od parametru funkcji, default arguments od keyword arguments?

# argument funckji to wartosc przekazywana do funkcji podczas jej wywoływania
# parametr funkcji jest to nazwa zmiennej lokalnej do której ląduje adres przekazywanego argumentu

# default arguments to argumenty które posiadaja domysle wartosci. W przypadku kiedy chcemy nadpisac argument mozemy to zrobic.
# ketword arguments to argumenty które podczas przekazywania sa zwiazane z nazwa parametru funkcji. Umozliwia to podawanie argumentow do funkcji w dowolnej kolejnosci
# oraz pozwala upewnic sie ze argument zostanie sparowany z odpowiednim parametrem
